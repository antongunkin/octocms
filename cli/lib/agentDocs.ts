export function generateAgentOverview(cfg: Config, collectionNames: readonly string[]): string {
  const lines: string[] = [AGENT_DOCS_BANNER + '# OctoCMS — Content Management for AI Agents', ''];

  // Intro
  lines.push(
    'This document describes how to manage content in an OctoCMS project directly via file operations.',
    'Use it when an AI agent needs to create, read, update, or delete content entries without the admin UI.',
    '',
  );

  // Content storage
  lines.push('## How content is stored', '');
  lines.push(
    `Content lives in the \`${cfg.contentFolder}/\` directory as JSON files. Each collection has its own subfolder.`,
    '',
    '**File naming:**',
    `- **\`hasMany\` collections** (multiple entries): \`${cfg.contentFolder}/{type}/{type}-{uuid}.json\` — UUID is generated via \`crypto.randomUUID()\``,
    `- **Singleton collections** (single entry): \`${cfg.contentFolder}/{type}/{type}-0000.json\` — fixed ID \`0000\``,
    '',
  );

  // Schema source of truth
  lines.push('## Where the schema lives', '');
  lines.push(
    'The data model is defined in **`cms/schema.json`** — the source of truth. It is plain JSON, hand-editable, and the visual Content Model editor at `/cms/model` writes to the same file.',
    '',
    '`cms/octocms.config.ts` is a thin TypeScript binding that re-exports `cms/__generated__/schema.ts` (a literal-typed mirror of `cms/schema.json` regenerated by `npm run types:gen`). Editing `cms/octocms.config.ts` directly is **not** supported — edit `cms/schema.json` instead.',
    '',
    'See [editing-schema.md](./editing-schema.md) for how to edit `cms/schema.json` safely (validation invariants, regeneration commands, and migration semantics).',
    '',
  );

  // Collections summary
  lines.push('## Collections', '');
  lines.push('| Collection | Label | Type |');
  lines.push('| --- | --- | --- |');
  for (const key of collectionNames) {
    const col = cfg.collections[key];
    const kind = col.hasMany ? 'hasMany' : 'singleton';
    lines.push(`| \`${key}\` | ${col.label} | ${kind} |`);
  }
  lines.push('');

  // Entry structure
  lines.push('## Entry JSON structure', '');
  lines.push(
    'Every entry file has this shape:',
    '',
    '```json',
    '{',
    '  "sys": {',
    '    "id": "<uuid-or-fixed-id>",',
    '    "type": "<collectionName>",',
    '    "status": "draft"',
    '  },',
    '  "fields": {',
    '    "fieldName": "value"',
    '  }',
    '}',
    '```',
    '',
  );

  // Status
  lines.push('## Entry status values', '');
  lines.push('| Status | Meaning | Visible on public site |');
  lines.push('| --- | --- | --- |');
  lines.push('| `draft` | Newly created, never published | No |');
  lines.push('| `published` | Live on public site | Yes |');
  lines.push('| `changed` | Edited after publish, not yet re-published | Yes (shows latest) |');
  lines.push('| `merged` | On main branch, baseline state | Yes |');
  lines.push('| `archived` | Soft-deleted | No |');
  lines.push('');
  lines.push('When creating a new entry, set `"status": "draft"`.', '');

  // Companion files
  const hasCompanion = collectionNames.some((key) => {
    const col = cfg.collections[key];
    return Object.values(col.fields).some((f) => f.format === 'markdown' || f.format === 'richtext');
  });

  if (hasCompanion) {
    lines.push('## Companion files (markdown & richtext)', '');
    lines.push(
      'Fields with `format: "markdown"` or `format: "richtext"` are **not** stored in the JSON entry.',
      'Instead, they use companion files alongside the entry:',
      '',
      '- **Markdown:** `{type}-{id}.{fieldName}.md`',
      '- **Richtext:** `{type}-{id}.{fieldName}.mdx`',
      '',
      'Example: A post entry `post-abc123.json` with a `body` markdown field stores its content in `post-abc123.body.md` in the same directory.',
      '',
    );
  }

  // URL mapping
  const publicCollections = cfg.search?.publicCollections;
  if (publicCollections && Object.keys(publicCollections).length > 0) {
    lines.push('## URL to collection mapping', '');
    lines.push('Use this table to match a public URL to its content collection and entry:');
    lines.push('');
    lines.push('| URL pattern | Collection | How to find the entry |');
    lines.push('| --- | --- | --- |');
    for (const [colName, searchCfg] of Object.entries(publicCollections)) {
      const col = cfg.collections[colName];
      if (!col) continue;
      const params = searchCfg.urlPattern.match(/:(\w+)/g)?.map((p) => p.slice(1)) ?? [];
      const findHint =
        params.length > 0
          ? `Match \`fields.${params[0]}\` from the URL segment`
          : 'Fixed path — look up the singleton entry';
      lines.push(`| \`${searchCfg.urlPattern}\` | \`${colName}\` (${col.label}) | ${findHint} |`);
    }
    lines.push('');
  }

  // CRUD operations
  lines.push('## Creating a new entry', '');
  lines.push('1. Generate a UUID (e.g. `crypto.randomUUID()`)');
  lines.push(`2. Create \`${cfg.contentFolder}/{type}/{type}-{uuid}.json\` with the entry JSON structure above`);
  lines.push('3. Set `sys.status` to `"draft"`');
  lines.push('4. Fill in all required fields (see schema reference)');
  lines.push('5. For any markdown/richtext fields, create the companion file in the same directory');
  lines.push('6. Run `npm run types:gen` if you modified `cms/octocms.config.ts`');
  lines.push('');

  lines.push('## Updating an entry', '');
  lines.push('1. Read the JSON file');
  lines.push('2. Modify values in the `fields` object');
  lines.push('3. Write the file back');
  lines.push('4. For markdown/richtext fields, edit the companion `.md`/`.mdx` file directly');
  lines.push('');

  lines.push('## Deleting an entry', '');
  lines.push('1. Delete the JSON file');
  lines.push('2. Delete any companion files (`{type}-{id}.*.md`, `{type}-{id}.*.mdx`)');
  lines.push('');

  // Adding a collection
  lines.push('## Adding a new collection', '');
  lines.push(
    '1. Edit `cms/schema.json` — add the new collection key under `collections` (see [editing-schema.md](./editing-schema.md) for the JSON shape and validation rules)',
  );
  lines.push(`2. Create the content directory: \`${cfg.contentFolder}/{collectionName}/\``);
  lines.push(
    '3. Run `npm run types:gen` to regenerate `cms/__generated__/` (TypeScript types, query helper, literal-typed schema shim)',
  );
  lines.push('4. Run `npm run docs:gen` and `npm run agent-docs:gen` to refresh `docs/generated/` and `octocms/docs/`');
  lines.push('5. Optionally create a public page route using the `query()` API:', '');
  lines.push('```typescript');
  lines.push("import { query } from 'octocms/query';");
  lines.push('');
  lines.push("const entries = await query('collectionName')");
  lines.push("  .sort('fieldName', 'desc')");
  lines.push('  .toArray();');
  lines.push('```');
  lines.push('');

  return lines.join('\n');
}

