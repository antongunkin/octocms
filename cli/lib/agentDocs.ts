export function generateAgentIndex(): string {
  const lines: string[] = [AGENT_DOCS_BANNER + '# OctoCMS — AI Agent Documentation', ''];

  lines.push(
    'These auto-generated docs describe how AI agents can manage content in an OctoCMS project.',
    'Include them in your `AGENTS.md` or reference them directly.',
    '',
    '## Documents',
    '',
    '- **[Content Management](./overview.md)** — How to find, create, update, and delete content entries via file operations *(auto-generated)*',
    '- **[Schema Reference](./schema.md)** — Per-collection field definitions, example JSON, and file path conventions *(auto-generated)*',
    '- **[Editing the Schema](./editing-schema.md)** — How to edit `cms/schema.json` directly — file location, structure, validation invariants, and what to regenerate after editing *(hand-written, stable)*',
    '',
    '## Usage in AGENTS.md',
    '',
    "Reference these docs from your project's `AGENTS.md`:",
    '',
    '```markdown',
    '## Content Management',
    '',
    'See `octocms/docs/overview.md` and `octocms/docs/schema.md` for how to manage CMS content directly.',
    'See `octocms/docs/editing-schema.md` for how to edit `cms/schema.json`.',
    '```',
    '',
    `To regenerate after schema changes: \`npm run agent-docs:gen\``,
    '',
  );

  return lines.join('\n');
}
